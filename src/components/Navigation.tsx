import { NavLink } from 'react-router-dom'
import { useGoogleDriveContext } from '../contexts/GoogleDriveContext'

const navItems = [
  { to: '/', label: 'Dziś', icon: HomeIcon },
  { to: '/meals', label: 'Historia', icon: ListIcon },
  { to: '/family', label: 'Rodzina', icon: UsersIcon },
  { to: '/settings', label: 'Ustawienia', icon: SettingsIcon },
]

export default function Navigation() {
  const { isConnected, isSyncing, isConfigured, hasUnsyncedChanges } = useGoogleDriveContext()

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white border-t border-gray-200 safe-bottom">
      <div className="flex justify-around items-center h-16 max-w-lg mx-auto">
        {navItems.map((item) => (
          <NavLink
            key={item.to}
            to={item.to}
            className={({ isActive }) =>
              `flex flex-col items-center justify-center w-full h-full transition-colors ${
                isActive
                  ? 'text-primary-500'
                  : 'text-gray-500 hover:text-gray-700'
              }`
            }
          >
            <item.icon className="w-6 h-6" />
            <span className="text-xs mt-1">{item.label}</span>
          </NavLink>
        ))}

        {isConfigured && (
          <NavLink
            to="/settings"
            className="flex flex-col items-center justify-center w-full h-full transition-colors text-gray-500 hover:text-gray-700"
            title={isConnected ? 'Połączono z Google Drive' : 'Połącz z Google Drive'}
          >
            <div className="relative">
              {isSyncing ? (
                <LoaderIcon className="w-6 h-6 animate-spin text-primary-500" />
              ) : (
                <CloudIcon
                  className={`w-6 h-6 ${
                    isConnected && hasUnsyncedChanges
                      ? 'text-yellow-500'
                      : isConnected
                      ? 'text-green-500'
                      : ''
                  }`}
                />
              )}
            </div>
            <span className="text-xs mt-1">Sync</span>
          </NavLink>
        )}
      </div>
    </nav>
  )
}

